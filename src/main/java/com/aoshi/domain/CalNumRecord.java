package com.aoshi.domain;

public class CalNumRecord {
    private Integer numRecordId;

    private Integer recordNum;

    private Integer prizeId;

    private Integer numSetId;

    public Integer getNumRecordId() {
        return numRecordId;
    }

    public void setNumRecordId(Integer numRecordId) {
        this.numRecordId = numRecordId;
    }

    public Integer getRecordNum() {
        return recordNum;
    }

    public void setRecordNum(Integer recordNum) {
        this.recordNum = recordNum;
    }

    public Integer getPrizeId() {
        return prizeId;
    }

    public void setPrizeId(Integer prizeId) {
        this.prizeId = prizeId;
    }

    public Integer getNumSetId() {
        return numSetId;
    }

    public void setNumSetId(Integer numSetId) {
        this.numSetId = numSetId;
    }
}